--[[
Strategys:
1. Precision
    Calculate where the target will be when the flak arrives, and fire at that location consistently.
    Triggers:
        - Target is moving in straight line
        - Target is not changing course often
        - Target is at high altitude
2. Burst Fire
    Nobody fires for 30-60 seconds. Then all flak fire at the target at once using target prediction like the precision mode. Each flak will fire 2 shells 
]]